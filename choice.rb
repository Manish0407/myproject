module Choice
  def Choice.user
    puts "\t \tPress 1. for issue book."
    puts "\t \tPress 2. for return book."
    puts "\t \tPress 3. for add a book."
    puts "\t \tPress 4. for book details."
    puts "\t \tPress 5 for borrower details."
    puts "\t \tPress 6. for exit."
    print "\t \tPlease enter your choice : "
    choice = gets.to_i
    Library.library(choice)
  end

  def Choice.repeat
    puts "=============================================================================================================================================="
    print "\t \tDo you want to continue (yes/no) :"
    input = gets.chomp

    if input == "yes"
      puts "=============================================================================================================================================="
      puts "...............................................................Welcome again!................................................................."
      puts "=============================================================================================================================================="
      Choice.user
    else
      File.truncate("details.txt", 0)
      puts "=============================================================================================================================================="
      puts "...............................................................Thank You!................................................................."
      puts "=============================================================================================================================================="
      exit
    end
  end
end
